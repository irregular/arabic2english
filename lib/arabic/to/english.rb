module Arabic
  module To
    module English

		def convert number

			case number

			when 1

				"one"

			when 2

				"two"

			when 3

				"three"

			when 4

				"four"

			when 5

				"five"

			when 6

				"six"

			when 7

				"seven"

			when 8

				"eight"

			when 9

				"nine"

			when 10

				"ten"

			end

		end

		module_function :convert

    end
  end
end
